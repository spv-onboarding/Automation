package com.supervielle.backend.domain.alta_persona.request;

public class InfAdicional {

    private String PAT;

    private String ingresoRealMensual;

    private String nivelEstudio;

    private String codAutonomo;

    public String getPAT() {
        return PAT;
    }

    public void setPAT(String PAT) {
        this.PAT = PAT;
    }

    public String getIngresoRealMensual() {
        return ingresoRealMensual;
    }

    public void setIngresoRealMensual(String ingresoRealMensual) {
        this.ingresoRealMensual = ingresoRealMensual;
    }

    public String getNivelEstudio() {
        return nivelEstudio;
    }

    public void setNivelEstudio(String nivelEstudio) {
        this.nivelEstudio = nivelEstudio;
    }

    public String getCodAutonomo() {
        return codAutonomo;
    }

    public void setCodAutonomo(String codAutonomo) {
        this.codAutonomo = codAutonomo;
    }

}
